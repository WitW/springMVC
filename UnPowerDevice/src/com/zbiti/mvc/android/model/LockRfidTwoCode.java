package com.zbiti.mvc.android.model;

public class LockRfidTwoCode {
	private String lockRfid;//设备锁的Rfid
	private String lockTwoCode;//设备锁的二维码
	private String area;//地区
	private String staffId;//用户名
	private String deviceNo;//设备Id
	
	private String longitude;//经度
	
	private String latitude;//维度
	
	private boolean forceUpdate;//是否为强制更新
	
	public boolean isForceUpdate() {
		return forceUpdate;
	}
	public void setForceUpdate(boolean forceUpdate) {
		this.forceUpdate = forceUpdate;
	}
	public String getLockRfid() {
		return lockRfid;
	}
	public void setLockRfid(String lockRfid) {
		this.lockRfid = lockRfid;
	}
	public String getLockTwoCode() {
		return lockTwoCode;
	}
	public void setLockTwoCode(String lockTwoCode) {
		this.lockTwoCode = lockTwoCode;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getStaffId() {
		return staffId;
	}
	public void setStaffId(String staffId) {
		this.staffId = staffId;
	}
	public String getDeviceNo() {
		return deviceNo;
	}
	public void setDeviceNo(String deviceNo) {
		this.deviceNo = deviceNo;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	
}
