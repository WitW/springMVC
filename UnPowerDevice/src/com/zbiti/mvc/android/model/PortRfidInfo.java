package com.zbiti.mvc.android.model;

/**
 * 
 * 端口RFID 信息
 *
 */
public class PortRfidInfo {
	
	private String staffId ; //操作人
	
	private String area ;	//地区

	private String device_id;	//操作设备ID
	
	private String phy_port_id;		//操作端口ID
	
	private String phy_port_opr;	//操作类型（绑定/解绑）
	
	private String new_port_rf_id;	//新端口RFID
		
	private String old_port_rf_id;	//旧端口RFID
	
	private String new_fiber_rf_id;	//新光纤接头RFID
	
	private String old_fiber_rf_id;	//新光纤接头RFID
	
	private String notes;			//备注
	
	public String getStaffId() {
		return staffId;
	}

	public void setStaffId(String staffId) {
		this.staffId = staffId;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getPhy_port_opr() {
		return phy_port_opr;
	}

	public void setPhy_port_opr(String phyPortOpr) {
		phy_port_opr = phyPortOpr;
	}

	public String getDevice_id() {
		return device_id;
	}

	public void setDevice_id(String deviceId) {
		device_id = deviceId;
	}

	public String getPhy_port_id() {
		return phy_port_id;
	}

	public void setPhy_port_id(String phyPortId) {
		phy_port_id = phyPortId;
	}

	public String getNew_port_rf_id() {
		return new_port_rf_id;
	}

	public void setNew_port_rf_id(String newPortRfId) {
		new_port_rf_id = newPortRfId;
	}

	public String getOld_port_rf_id() {
		return old_port_rf_id;
	}

	public void setOld_port_rf_id(String oldPortRfId) {
		old_port_rf_id = oldPortRfId;
	}

	public String getNew_fiber_rf_id() {
		return new_fiber_rf_id;
	}

	public void setNew_fiber_rf_id(String newFiberRfId) {
		new_fiber_rf_id = newFiberRfId;
	}

	public String getOld_fiber_rf_id() {
		return old_fiber_rf_id;
	}

	public void setOld_fiber_rf_id(String oldFiberRfId) {
		old_fiber_rf_id = oldFiberRfId;
	}

	public String getNotes() {
		return notes;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}
	
}
